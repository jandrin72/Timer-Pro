class TranslationUtil {
  static defaultLanguage = 'es';
  static fallbackLanguage = 'en';
  static storageKey = 'timer_pro_language';
  static translations = TRANSLATIONS;
  static currentLanguage = null;

  static hasLanguage(lang) {
    return Object.prototype.hasOwnProperty.call(TRANSLATIONS, lang);
  }

  static readStoredLanguage() {
    try {
      if (typeof window !== 'undefined' && window.StorageUtil) {
        const stored = StorageUtil.get(this.storageKey);
        if (typeof stored === 'string') return stored;
      } else if (typeof localStorage !== 'undefined') {
        const stored = localStorage.getItem(this.storageKey);
        if (stored) return stored;
      }
      if (typeof localStorage !== 'undefined') {
        const legacy = localStorage.getItem('app_language');
        if (legacy) {
          this.persistLanguage(legacy);
          localStorage.removeItem('app_language');
          return legacy;
        }
      }
    } catch (error) {
      console.warn('Unable to read language from storage:', error);
    }
    return null;
  }

  static persistLanguage(lang) {
    try {
      if (typeof window !== 'undefined' && window.StorageUtil) {
        StorageUtil.set(this.storageKey, lang);
      } else if (typeof localStorage !== 'undefined') {
        localStorage.setItem(this.storageKey, lang);
      }
    } catch (error) {
      console.warn('Unable to persist language to storage:', error);
    }
  }

  static detectLanguage() {
    const stored = this.readStoredLanguage();
    if (stored && this.hasLanguage(stored)) {
      return stored;
    }
    if (typeof navigator !== 'undefined' && navigator.language) {
      const browserLang = navigator.language.split('-')[0];
      if (this.hasLanguage(browserLang)) {
        return browserLang;
      }
    }
    return this.defaultLanguage;
  }

  static init() {
    if (!this.currentLanguage) {
      this.currentLanguage = this.detectLanguage();
    }
    return this.currentLanguage;
  }

  static getLanguage() {
    return this.init();
  }

  static setLanguage(lang) {
    if (!this.hasLanguage(lang)) {
      console.warn(`Idioma no soportado: ${lang}`);
      return false;
    }
    this.currentLanguage = lang;
    this.persistLanguage(lang);
    return true;
  }

  static getTranslations(lang) {
    return TRANSLATIONS[lang] || null;
  }

  static getAvailableLanguages() {
    return Object.keys(TRANSLATIONS);
  }

  static formatFallback(key) {
    return key
      .split('_')
      .map(part => part.charAt(0).toUpperCase() + part.slice(1))
      .join(' ');
  }

  static t(key, params = {}) {
    const lang = this.getLanguage();
    const translations = TRANSLATIONS[lang] || {};
    let template = translations[key];

    if (template === undefined) {
      const fallbackTranslations =
        TRANSLATIONS[this.fallbackLanguage] || TRANSLATIONS[this.defaultLanguage] || {};
      template = fallbackTranslations[key];
    }

    if (template === undefined) {
      template = this.formatFallback(key);
    }

    return template.replace(/\{(.*?)\}/g, (match, placeholder) => {
      if (Object.prototype.hasOwnProperty.call(params, placeholder)) {
        return params[placeholder];
      }
      return match;
    });
  }
}

TranslationUtil.init();

window.t = (key, params) => TranslationUtil.t(key, params);
window.TranslationUtil = TranslationUtil;
